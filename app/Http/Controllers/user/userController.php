    public function rankup(Request $request)
    {
        $id = $request->userid;
        $foundUser = User::find($id);
        if ($foundUser->accountRole == 1) {
            $foundUser->accountRole = $foundUser->accountRole + 1;
                $foundUser->save();
            return Redirect::to('admin/user');
        }elseif ($foundUser->accountRole <= 2) {
